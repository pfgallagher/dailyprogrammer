const goldilocks = ([[maxWeight, maxTemp], ...rest]: [
	number,
	number,
][]): number[] => {
	const res: number[] = [];
	rest.forEach(([weight, temp], i) => {
		if (weight >= maxWeight && temp <= maxTemp) {
			res.push(i + 1);
		}
	});
	return res;
};

console.log(
	goldilocks([
		[100, 80],
		[30, 50],
		[130, 75],
		[90, 60],
		[150, 85],
		[120, 70],
		[200, 200],
		[110, 100],
	]),
);
// [2, 5]

