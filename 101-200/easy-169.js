var rotate2DArr = function (n, arr) {
    return arr
        .reduce(function (acc, cur) {
        cur.forEach(function (el, i) {
            acc[i].push(el);
        });
        return acc;
    }, Array(n)
        .fill(0)
        .map(function () { return []; }))
        .map(function (el) { return el.reverse(); });
};
console.log(rotate2DArr(3, [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9],
]));
// [[7, 4, 1],
// [8, 5, 2],
// [9, 6, 3]]
console.log(rotate2DArr(3, [
    [7, 4, 1],
    [8, 5, 2],
    [9, 6, 3],
]));
// [[9, 8, 7],
// [6, 5, 4],
// [3, 2, 1]]
