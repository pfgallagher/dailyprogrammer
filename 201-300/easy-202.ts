import { chunkArr } from "../lib/util";

const binaryToEnglish = (input: string): string =>
	(chunkArr([...input], 8) as string[][])
		.map(arr => String.fromCharCode(parseInt(arr.join(""), 2)))
		.join("");

console.log(
	binaryToEnglish(
		"0100100001100101011011000110110001101111001000000101011101101111011100100110110001100100",
	),
);
